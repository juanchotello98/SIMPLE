    private void registerButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtomActionPerformed
        // TODO add your handling code here:

        String identification;
        String name;
        String description;
        String managerID;

        if(identificationTextField.getText().length()>0 && nameTextField.getText().length()>0 && descriptionTextArea.getText().length()>0){

            identification=identificationTextField.getText();
            name=nameTextField.getText();
            description=descriptionTextArea.getText();
            managerID = projectControl.getManagerID((String)managerComboBox.getSelectedItem());

            int numFilas = projectControl.insertProject(identification, name, description, managerID);

            System.out.println ("Filas "+ numFilas);
            if (numFilas == 1){

                JOptionPane.showMessageDialog(null, "Equipo guardado exitosamente");
                limpiar_texto();
            }
            else {
                JOptionPane.showMessageDialog(null, "Ocurrio un problema al guardar el Equipo");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }
    }//GEN-LAST:event_registerButtomActionPerformed

