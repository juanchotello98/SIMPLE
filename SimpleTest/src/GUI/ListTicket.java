    public String traduction(String word){
        switch (word) {
            case "Codigo":
             word = "code";
             break;
             case "Nombre":
             word = "firstName";
             break;
             case "Apellido":
             word = "lastName";
             break;
             case "Articulo":
             word = "serialNumber";
             break;
             case "Estado":
             word = "state";
             break;
             case "Tipo Multa":
             word = "tipoMulta";
             break;
             default:
             System.out.println("error");
             break;} 
             
             return  word;
        }
    
