    private void payButtonActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payButtonActionPerformedActionPerformed
        // TODO add your handling code here:
        int row = ticketTable.getSelectedRow();
        if(row!=-1){
            if(ticketControl.verifyTicket(ticketTable.getValueAt(row, 0).toString())==0){
                ticketControl.payTicket(ticketTable.getValueAt(row, 0).toString());
                JOptionPane.showMessageDialog(null, "A pagado exitosamente la multa.");
                loadTickets();
            } else{
                if(ticketControl.verifyTicket(ticketTable.getValueAt(row, 0).toString())==1){
                    JOptionPane.showMessageDialog(null, "Ya a pagado esta multa.");
                } else{
                    JOptionPane.showMessageDialog(null, "No puede pagar una multa que se a cancelado.");
                }
            }                      
        }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_payButtonActionPerformedActionPerformed

    private void cancelTicketButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelTicketButtomActionPerformed
        // TODO add your handling code here:
        int row = ticketTable.getSelectedRow();
        if(row!=-1){
            if(ticketControl.verifyTicket(ticketTable.getValueAt(row, 0).toString())==0){
                ticketControl.cancelTicket(ticketTable.getValueAt(row, 0).toString());
                JOptionPane.showMessageDialog(null, "A cancelado exitosamente la multa.");
                loadTickets();
            } else{
                if(ticketControl.verifyTicket(ticketTable.getValueAt(row, 0).toString())==1){
                    JOptionPane.showMessageDialog(null, "No puede cancelar una multa que ya se pago.");
                } else{
                    JOptionPane.showMessageDialog(null, "Esta multa ya esta cancelada.");
                }
            }                      
        }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_cancelTicketButtomActionPerformed

    private void searchButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtomActionPerformed
        // TODO add your handling code here:
        Ticket ticket;
        String inputValue;
        String typeSearch;

        if(nameTextField.getText().length()>0){

            inputValue=nameTextField.getText();
            typeSearch=(String)typeSearchCombo.getSelectedItem();
            typeSearch = traduction(typeSearch);

            ticket = ticketControl.searchTicket(inputValue, typeSearch);
            if(ticket.getCode()!= null){
                JOptionPane.showMessageDialog(null,"Bùsqueda Exitosa");
                listSearchTicket(inputValue,typeSearch);
            }
            else{
                JOptionPane.showMessageDialog(null,"No se encontrò ninguna Multa que coincida con la bùsqueda");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }

    }//GEN-LAST:event_searchButtomActionPerformed

