    private void registerUserButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerUserButtomActionPerformed
        // TODO add your handling code here:

        String identification;
        String password;
        String firstName;
        String lastName;
        String email;
        String charge;
        String typeID;
        String phone;
        

        if(identificationTextField.getText().length()>0 && firstNameTextField.getText().length()>0 && lastNameTextField.getText().length()>0
           && emailTextField.getText().length()>0 && phoneTextField.getText().length()>0){

            identification=identificationTextField.getText();
            firstName=firstNameTextField.getText();
            lastName=lastNameTextField.getText();
            email=emailTextField.getText();
            charge=(String)chargeComboBox.getSelectedItem();
            typeID=(String)typeIDComboBox.getSelectedItem();
            phone=phoneTextField.getText();
            password=userControl.assingPassword(firstName, lastName, identification);

            int numFilas = userControl.insertUser(identification, password, firstName, lastName, email, charge, typeID, phone);

            System.out.println ("Filas "+ numFilas);
            if (numFilas == 1){

                JOptionPane.showMessageDialog(null, "Usuario guardado exitosamente");
                limpiar_texto();
            }
            else {
                JOptionPane.showMessageDialog(null, "Ocurrio un problema al guardar el Usuario");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }
    }//GEN-LAST:event_registerUserButtomActionPerformed

