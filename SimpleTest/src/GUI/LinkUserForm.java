    public void listSearchUser(String inputValue, String typeSearch,String identificationProject){
            
        ArrayList <Object[]> users = new ArrayList <>();
        users = userControl.listSearchLinkUser(inputValue, typeSearch, identificationProject);
            
        DefaultTableModel modelo = (DefaultTableModel) userTable.getModel();
        modelo.setRowCount(0);  

        for(int i=0; i<users.size(); i++ ){
        modelo.addRow(users.get(i));
        userTable.setModel(modelo);
            }     
        }
    
    public String traduction(String word){
        
        switch (word) {
           case "Identificacion":
             word = "identification";
             break;
             case "Nombre":
             word = "firstName";
             break;
             case "Apellido":
             word = "lastName";
             break;
             case "Email":
             word = "email";
             break;
             default:
             System.out.println("error");
             break;} 
             
             return  word;
        }
    
    public void loadUsers(String projectID, String userID){
        
    ArrayList <Object[]> users = new ArrayList <>();
    users = userControl.listUserLinkProject(projectID, userID);
            
    DefaultTableModel modelo = (DefaultTableModel) userTable.getModel();
    modelo.setRowCount(0);  

    for(int i=0; i<users.size(); i++ ){
    modelo.addRow(users.get(i));
    userTable.setModel(modelo);
            }     
    }

