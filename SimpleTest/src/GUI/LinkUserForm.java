    private void searchButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtomActionPerformed
        // TODO add your handling code here:
        User user;
        String inputValue;
        String typeSearch;

        if(nameTextField.getText().length()>0){

            inputValue=nameTextField.getText();
            typeSearch=(String)typeSearchCombo.getSelectedItem();
            typeSearch = traduction(typeSearch);

            user = userControl.searchLinkUser(inputValue, typeSearch, identificationProject);

            if(user.getCode()!= null){

                JOptionPane.showMessageDialog(null,"Bùsqueda Exitosa");
                listSearchUser(inputValue,typeSearch,identificationProject);

            }
            else{
                JOptionPane.showMessageDialog(null,"No se encontrò ningun Equipo que coincida con la bùsqueda");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }
    }//GEN-LAST:event_searchButtomActionPerformed

    private void LinkUserButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LinkUserButtomActionPerformed
        // TODO add your handling code here:
        int row = userTable.getSelectedRow();
        if(row!=-1){
            
        String code=userTable.getValueAt(row, 0).toString();
        String identification=userTable.getValueAt(row, 1).toString();
        

        int numFilas = projectControl.linkUser(codeProject, identificationProject, code, identification);

            System.out.println ("Filas "+ numFilas);
            if (numFilas == 1){

                JOptionPane.showMessageDialog(null, "Usuario vinculado exitosamente");
                loadUsers(identificationProject, sessionUser.getIdentification());
            }
            else {
                JOptionPane.showMessageDialog(null, "Ocurrio un problema al vincular el usuario");
            }
         }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
      }
    }//GEN-LAST:event_LinkUserButtomActionPerformed

