    public void setValues(String code, String nameUser, String nameEquipment){
        Loan loan = new Loan();
        
        String startDate;
        String finalDate;
        String deliverDate;
        
        loan = loanControl.getInfoLoan(code);
        Format formatter = new SimpleDateFormat("yyyy-MM-dd");
        
        if(loan.getStartDate() != null){
        startDate = formatter.format(loan.getStartDate());
        finalDate = formatter.format(loan.getFinalDate());
        }
        else{startDate="";
             finalDate="";
             deliverDate="";      
        }
        
        if(loan.returnEquipment() != null){
        deliverDate = formatter.format(loan.returnEquipment());
        }
        else {deliverDate="";
        }
        
        String renew=Integer.toString(loan.getRenew());
        
        codeText.setText(loan.getCode());
        userNameText.setText(nameUser);
        userIDText.setText(loan.getUserCode());
        equipmentNameText.setText(nameEquipment);
        equipmentIDText.setText(loan.getEquipmentCode());
        startDateText.setText(startDate);
        finalDateText.setText(finalDate);
        deliverDateText.setText(deliverDate);
        stateText.setText(loan.getState());
        renewText.setText(renew);
        
        userNameText.setVisible(false);
        userIDText.setVisible(false);
        nameLabel.setVisible(false);
        userIDLabel.setVisible(false);
     
                  
    }

