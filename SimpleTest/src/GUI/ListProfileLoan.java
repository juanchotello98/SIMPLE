    public void loadProfileLoans(){
        
        ArrayList <Object[]> myLoans = new ArrayList <>();
        myLoans = loanControl.listProfileLoan(sessionUser.getIdentification());

        DefaultTableModel modelo = (DefaultTableModel) loanTable.getModel();
        modelo.setRowCount(0);  

        for(int i=0; i<myLoans.size(); i++ ){
            System.out.println(myLoans.size());
            modelo.addRow(myLoans.get(i));
            loanTable.setModel(modelo);
        }     
    }
    
