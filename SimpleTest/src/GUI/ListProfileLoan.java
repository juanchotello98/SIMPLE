    public String traduction(String word){
            
        switch (word) {
            case "Codigo":
             word = "code";
             break;
             case "Articulo":
             word = "serialNumber";
             break;
             case "Estado":
             word = "state";
             break;
             default:
             System.out.println("error");
             break;} 
             
             return  word;
        }

