    private void deliverButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deliverButtomActionPerformed
        // TODO add your handling code here:
        int row = loanTable.getSelectedRow();
        if(row!=-1){
            if(loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())==0){
                loanControl.returnEquipment(loanControl.getLoan(loanTable.getValueAt(row, 0).toString()));
                loadProfileLoans();
                JOptionPane.showMessageDialog(null, "Equipo entregado exitosamente.");
                
            } else{
                
                if((loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())==1) || (loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())==2)){
                    JOptionPane.showMessageDialog(null, "Ya a entregado este equipo.");
                } else{
                    loanControl.returnEquipment(loanControl.getLoan(loanTable.getValueAt(row, 0).toString()));
                    loadProfileLoans();
                    JOptionPane.showMessageDialog(null, "El equipo a sido entregado exitosamente, por entregarlo fuera de tiempo a recibido una multa.");
                }
            }
                       
        }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_deliverButtomActionPerformed

    private void renewButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renewButtomActionPerformed
        // TODO add your handling code here:
    int row = loanTable.getSelectedRow();
        if(row!=-1){
            if(loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())== 0){
            if(loanControl.renewLoan(loanControl.getLoan(loanTable.getValueAt(row, 2).toString())) == 0){
                JOptionPane.showMessageDialog(null, "Equivo renovado exitosamente.");
                loadProfileLoans();
            } else{
                if(loanControl.renewLoan(loanControl.getLoan(loanTable.getValueAt(row, 2).toString())) == 1){
                    JOptionPane.showMessageDialog(null, "Ya no puede renovar mas este equipo, numero de renovaciones 3");
                } else {
                    JOptionPane.showMessageDialog(null, "Solo se puede renovar entre los 3 dias antes de que acabe el prestamo.");
                }
            }
        } else JOptionPane.showMessageDialog(null, "No puede renovar un prestamo que no este Activo");
        }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_renewButtomActionPerformed

    private void searchButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtomActionPerformed
        // TODO add your handling code here:
        Loan loan;
        String inputValue;
        String typeSearch;

        if(nameTextField.getText().length()>0){

            inputValue=nameTextField.getText();
            typeSearch=(String)typeSearchCombo.getSelectedItem();
            typeSearch = traduction(typeSearch);

            loan = loanControl.searchProfileLoan(inputValue, typeSearch, sessionUser.getIdentification());
            if(loan.getCode()!= null){
                JOptionPane.showMessageDialog(null,"Bùsqueda Exitosa");
                listSearchProfileLoan(inputValue,typeSearch,sessionUser.getIdentification());

            }
            else{
                JOptionPane.showMessageDialog(null,"No se encontrò ningun Equipo que coincida con la bùsqueda");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }
    }//GEN-LAST:event_searchButtomActionPerformed

