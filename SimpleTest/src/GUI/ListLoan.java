    public void loadAllLoans(){
        
        ArrayList <Object[]> allLoans = new ArrayList <>();
        allLoans = loanControl.listLoan();

        DefaultTableModel modelo = (DefaultTableModel) loanTable.getModel();
        modelo.setRowCount(0);  

        for(int i=0; i<allLoans.size(); i++ ){
            System.out.println(allLoans.size());
            modelo.addRow(allLoans.get(i));
            loanTable.setModel(modelo);
        }     
    }

    public void listSearchLoan(String inputValue, String typeSearch){
            
        ArrayList <Object[]> loans = new ArrayList <>();
        loans = loanControl.listSearchLoan(inputValue, typeSearch);

        DefaultTableModel modelo = (DefaultTableModel) loanTable.getModel();
        modelo.setRowCount(0);  

        for(int i=0; i<loans.size(); i++ ){
        modelo.addRow(loans.get(i));
        loanTable.setModel(modelo);
            }     
        }    
        
    public String traduction(String word){
            switch (word) {
            case "Codigo":
             word = "code";
             break;
             case "Articulo":
             word = "serialNumber";
             break;
             case "Estado":
             word = "state";
             break;
             default:
             System.out.println("error");
             break;} 
             
             return  word;
        }
