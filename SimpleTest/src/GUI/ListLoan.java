    private void validateDeliveryButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validateDeliveryButtomActionPerformed
        // TODO add your handling code here:
        int row = loanTable.getSelectedRow();
        if(row!=-1){
            if(loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())== 1){
                loanControl.validateDelivery(loanControl.getLoan(loanTable.getValueAt(row, 0).toString()));
                JOptionPane.showMessageDialog(null, "Validación de prestamo exitosa.");
                loadAllLoans();
            } else{
                if(loanControl.verifyLoan(loanTable.getValueAt(row, 0).toString())== 2) JOptionPane.showMessageDialog(null, "Ya a validado este prestamo.");
                else JOptionPane.showMessageDialog(null, "El prestamo sigue activo, solo se puede validar hasta que este 'Entregado'.");
            }             
        }
        else{ JOptionPane.showMessageDialog(null, "Por favor Seleccione una Fila");
        }
    }//GEN-LAST:event_validateDeliveryButtomActionPerformed

    private void searchButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtomActionPerformed
        // TODO add your handling code here:
        Loan loan;
        String inputValue;
        String typeSearch;

        if(nameTextField.getText().length()>0){

            inputValue=nameTextField.getText();
            typeSearch=(String)typeSearchCombo.getSelectedItem();
            typeSearch = traduction(typeSearch);

            loan = loanControl.searchLoan(inputValue, typeSearch);
            if(loan.getCode()!= null){
                JOptionPane.showMessageDialog(null,"Bùsqueda Exitosa");
                listSearchLoan(inputValue,typeSearch);

            }
            else{
                JOptionPane.showMessageDialog(null,"No se encontrò ningun Equipo que coincida con la bùsqueda");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor completa todos los campos");
        }
    }//GEN-LAST:event_searchButtomActionPerformed

